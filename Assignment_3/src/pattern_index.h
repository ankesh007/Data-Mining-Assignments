#ifndef _PATTERN_INDEX_H
#define	_PATTERN_INDEX_H

#include <set>
#include <string>
#include <vector>
using namespace std;

//class: pattern_index
/*to keep track of the codes of subgraph patterns that have been generated before
 in order to avoid repetitive consideration of the same subgraph pattern*/
class pattern_index {
public:
    set<vector<int> > data;	/*the code of each subgraph pattern is stored as vector<int>,
								 here "data" keeps track of the codes of all subgraph patterns 
								 that have been generated by the algorithm*/
    void insert(vector<int>& s);	/*insert a code "s" into "data"*/
    bool find(vector<int>& s);	/*find out if code "s" is already in "data"*/
};

#endif	/* _PATTERN_INDEX_H */

